#  The range function is used to generate a sequence of numbers 
# We can adjust the start, stop, and skip-size of the numbers.

for i in range(0, 10,1): # bydefault range(0(Start),10(Stop),1(skip value))
    print(i)
# it will stop 1 number before the stop value i.e. if stop value 10 it ill print upto 9
